    public int[][] generateIntBoard()
    {
        int[][] board = new int[9][9];
        
        for(int i = 0; i < 9; i++)
        {
            for(int j = 0; j < 9; j++)
            {
                if(this.board[i][j].getText().equals(" ") || this.board[i][j].getText().isEmpty())
                {
                    board[i][j] = 0;
                }
                else
                {
                    board[i][j] = Integer.parseInt(this.board[i][j].getText());
                }
            }
        }
        return board;
        
    }
    
    
    
    
