    public boolean solveBoard(int[][] board)
    {
         for (int row = 0; row < 9; row++) {
        for (int column = 0; column < 9; column++) {
            if (board[row][column] == 0) {
                for (int k = 1; k <= 9; k++) {
                    board[row][column] = k;
                    if (checkBoard(board) && solveBoard(board)) {
                        this.board[row][column].setText("" +k);
                        return true;
                    }
                    board[row][column] = 0;
                }
                
                return false;
            }
        }
    }
         return true;
   }
         
    
    
    public boolean verifyUnique(int[] ia)
    {

        Arrays.sort(ia);
        ArrayList<Integer> usedValues;
        usedValues = new ArrayList<>();
        
        
        
        for(int i = 0; i < ia.length; i++)
        {
            if(ia[i] == 0)
            {
                continue;
            }
            for(int j = 0; j < usedValues.size(); j++)
            {
                if(usedValues.get(j) == ia[i])
                {
                  
                    return false;
                }
                
                    
            }
            usedValues.add(ia[i]);    
                
        }
        
        return true;   
            
            
    }
